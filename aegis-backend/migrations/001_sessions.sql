COMMENT ON TABLE sessions IS 'Aegis Lens session management hyper-table with 5-minute TTL and 24-hour retention';
COMMENT ON COLUMN sessions.session_id IS 'Unique session identifier (UUID)';
COMMENT ON COLUMN sessions.public_key_pem IS 'Client ECDSA P-256 public key in PEM format';
COMMENT ON COLUMN sessions.client_nonce IS '4-byte random nonce generated by client';
COMMENT ON COLUMN sessions.server_nonce IS '4-byte random nonce generated by server';
COMMENT ON COLUMN sessions.created_at IS 'Session creation timestamp';
COMMENT ON COLUMN sessions.expires_at IS 'Session expiration timestamp (created_at + 5 minutes)';
COMMENT ON COLUMN sessions.status IS 'Session status: active, expired, revoked';
COMMENT ON COLUMN sessions.verdict IS 'Final verdict: CLEAR, SUSPICIOUS, BLOCKED';
