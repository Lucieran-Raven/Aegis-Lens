-- Aegis Lens v2.0 - Session Migration
-- TimescaleDB hyper-table for session management
-- This table stores ephemeral session data with 5-minute TTL for validation

-- Enable TimescaleDB extension
CREATE EXTENSION IF NOT EXISTS timescaledb;

-- Create schema versions table for migration tracking
CREATE TABLE IF NOT EXISTS schema_versions (
    version INTEGER PRIMARY KEY,
    applied_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    description TEXT NOT NULL
);

-- Insert initial schema version
INSERT INTO schema_versions (version, description)
    VALUES (1, 'Initial schema - sessions and events tables')
    ON CONFLICT (version) DO NOTHING;

-- Create sessions table
CREATE TABLE IF NOT EXISTS sessions (
    id BIGSERIAL PRIMARY KEY,
    session_id VARCHAR(64) UNIQUE NOT NULL,
    public_key_pem TEXT NOT NULL,
    client_nonce BYTEA NOT NULL,
    server_nonce BYTEA NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    expires_at TIMESTAMPTZ NOT NULL,
    client_ip VARCHAR(45),
    user_agent TEXT,
    status VARCHAR(20) DEFAULT 'active',

    -- Telemetry summary (for audit purposes only, no PII)
    last_verified_at TIMESTAMPTZ,
    verification_count INTEGER DEFAULT 0,
    total_score_avg NUMERIC(5,2),
    verdict VARCHAR(20)
);

